/*Operadores de comparação pt.2


>  -> Maior
<  -> Menor

>= -> Maior ou igual
<= -> Menor ou igual

Eles são usados para comparar valores e retornam um valor booleano (true ou false). Vamos ver como aplicá-los:
*/


const primeiroNumero = 30;
const segundoNumero = 20;

console.log (primeiroNumero > segundoNumero); // true
console.log (primeiroNumero < segundoNumero); // false

console.log (primeiroNumero >= segundoNumero); // true
console.log (primeiroNumero <= segundoNumero); // false